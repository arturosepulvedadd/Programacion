def area_rectangulo(base, altura):
    area = base * altura
    return area

# Programa Principal:

base_rectangulo = 15
altura_rectangulo = 10
area_del_rectangulo = area_rectangulo(base_rectangulo, altura_rectangulo)
print("El área del rectángulo con base", base_rectangulo, "y altura", altura_rectangulo, "es:", area_del_rectangulo)